package main

import (
	"flag"
	"fmt"
	"io/ioutil"
	"log"
	"time"

	"github.com/brotherlogic/goserver"
	"golang.org/x/net/context"
	"google.golang.org/grpc"

	pbg "github.com/brotherlogic/goserver/proto"
)

//Server main server type
type Server struct {
	*goserver.GoServer
}

// Init builds the server
func Init() *Server {
	s := &Server{
		GoServer: &goserver.GoServer{},
	}
	return s
}

// DoRegister does RPC registration
func (s *Server) DoRegister(server *grpc.Server) {

}

// ReportHealth alerts if we're not healthy
func (s *Server) ReportHealth() bool {
	return true
}

// Shutdown the server
func (s *Server) Shutdown(ctx context.Context) error {
	return nil
}

// Mote promotes/demotes this server
func (s *Server) Mote(ctx context.Context, master bool) error {
	return nil
}

// GetState gets the state of the server
func (s *Server) GetState() []*pbg.State {
	return []*pbg.State{
		&pbg.State{Key: "magic", Value: int64(12345)},
	}
}

func main() {
	var quiet = flag.Bool("quiet", false, "Show all output")
	flag.Parse()

	//Turn off logging
	if *quiet {
		log.SetFlags(0)
		log.SetOutput(ioutil.Discard)
	}
	server := Init()
	server.PrepServer()
	server.Register = server

	err := server.RegisterServerV2("basicjob", false, true)
	if err != nil {
		return
	}

	server.Log(fmt.Sprintf("Starting election"))
	time.Sleep(time.Second * 2)
	cancel, err := server.Elect()
	if err != nil {
		server.Log(fmt.Sprintf("Error performing election: %v", err))
		time.Sleep(time.Second * 30)
		return
	}
	server.Log(fmt.Sprintf("I have been elected"))
	time.Sleep(time.Second * 6)
	cancel()
	server.Log(fmt.Sprintf("ELECTION IS COMPLETE"))
	time.Sleep(time.Second * 5)

	fmt.Printf("%v", server.Serve())
}
